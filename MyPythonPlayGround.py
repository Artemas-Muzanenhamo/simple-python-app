class MyPythonPlayGround:
    # Integers and Floats
    # The format here is: Variable Name = Variable Value
    answer = 42


    # We can also have floats
    pi = 3.14159


    # Don't worry about conversion
    print(answer + pi) # 45.14159


    # Casting
    int(pi) == 3
    float(answer) == 42.0